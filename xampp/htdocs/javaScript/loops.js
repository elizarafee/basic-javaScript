
var i;
// For loop
for(i = 10; i > 0 ; i--){
    console.log('For loop no : ' + i);
}

var j = 0
//  While loop
while(j<10){
    console.log('While loop no : ', + j);
    j++;
}

var c = 1;
do{
    console.log('For while loop no : ' + c);
}while(c>5);


